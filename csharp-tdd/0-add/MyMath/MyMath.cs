using System;

namespace MyMath
{
    /// <summary>
    /// Defines the Operations Class
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Adds two ints
        /// </summary>
        /// <param name="a">First int</param>
        /// <param name="b">Second int</param>
        /// <returns>Result of addition a + b</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
